clear;
clc;
close all;

fprintf('CURRENT SOURCE CONVERTER INTERFACING PEM ELECTROLYSER\n');

% ============================================================
% PCC dq + PWM-CSC RL post-processing
% Two 3x2 figures:
%   1) dq variables
%   2) abc variables
% ============================================================

% ------------------------------------------------------------
% Input and output paths
% ------------------------------------------------------------
csvFile   = fullfile('results', 'csc_rl.csv');
figFolder = fullfile('results', 'figures');

if ~exist(figFolder, 'dir')
    mkdir(figFolder);
end

fprintf('\nReading CSV file...\n');
% ------------------------------------------------------------
% Read CSV file
% ------------------------------------------------------------
data = readtable(csvFile);

t = data.t;

% ------------------------------------------------------------
% Global plotting time window
% ------------------------------------------------------------
t_ini = 0;
t_fin = 5;

idx_plot = (t >= t_ini) & (t <= t_fin);

if ~any(idx_plot)
    error('Selected time window is empty. Check t_ini and t_fin.');
end

tp = t(idx_plot);

% ------------------------------------------------------------
% State vector
%
% x = [igd, igq, ed, eq, id, iq, vd, vq, istk, theta_hat, xi_pll]^T
% ------------------------------------------------------------
igd  = data.igd;
igq  = data.igq;
ed   = data.ed;
eq   = data.eq;
id   = data.id;
iq   = data.iq;
vd   = data.vd;
vq   = data.vq;
istk = data.istk;

theta_hat = data.theta_hat;
xi_pll    = data.xi_pll;
xi_ucd    = data.xi_ucd;
xi_ucq    = data.xi_ucq;
xi_isd    = data.xi_isd;
xi_isq    = data.xi_isq;
xi_idc2   = data.xi_idc2;

% ------------------------------------------------------------
% Algebraic outputs in abc
%
% C++ output names:
%   ia, ib, ic, va, vb, vc
% ------------------------------------------------------------
ia = data.ia;
ib = data.ib;
ic = data.ic;

ea = data.va;
eb = data.vb;
ec = data.vc;

P = data.P;
Q = data.Q;

idr = data.id_r;
iqr = data.iq_r;
idc_ref = data.idc_ref;   % DC current reference
Q_ref   = data.Q_ref;     % Reactive power reference
Pref    = data.Pref;      % Active power reference generated by outer loop



% ------------------------------------------------------------
% Filter states and algebraic outputs for plotting
% ------------------------------------------------------------
igd_p       = igd(idx_plot);
igq_p       = igq(idx_plot);
ed_p        = ed(idx_plot);
eq_p        = eq(idx_plot);

id_p        = id(idx_plot);
iq_p        = iq(idx_plot);
vd_p        = vd(idx_plot);
vq_p        = vq(idx_plot);
istk_p      = istk(idx_plot);

theta_hat_p = theta_hat(idx_plot);
xi_pll_p    = xi_pll(idx_plot);

ia_p = ia(idx_plot);
ib_p = ib(idx_plot);
ic_p = ic(idx_plot);

ea_p = ea(idx_plot);
eb_p = eb(idx_plot);
ec_p = ec(idx_plot);

P_p = P(idx_plot);
Q_p = Q(idx_plot);

idr_p = idr(idx_plot);
iqr_p = iqr(idx_plot);
idc_ref_p = idc_ref(idx_plot);
Q_ref_p   = Q_ref(idx_plot);
Pref_p    = Pref(idx_plot);

% ============================================================
% Parameters used only for derived variables
% ============================================================

pi_val = acos(-1.0);

Vbase = 380.0;
Sbase = 60e3;
wbase = 2.0*pi_val*50.0;

Zbase = Vbase^2/Sbase;
Tsw   = 1.0/10e3;
delta_i = 2.0;

RL = 0.6;

md = 0.8;
mq = 0.0;

% ------------------------------------------------------------
% DC-side quantities
% ------------------------------------------------------------
vdc_conv = md*vd + mq*vq;       % Converter-side average DC voltage
vstk     = RL*istk;             % Resistive stack voltage
Pdc      = vstk.*istk;          % Stack DC power

% ------------------------------------------------------------
% Filter derived variables for plotting
% ------------------------------------------------------------
P_p   = P(idx_plot);
Q_p   = Q(idx_plot);
Pdc_p = Pdc(idx_plot);

idc_ref_p = idc_ref(idx_plot);
Q_ref_p   = Q_ref(idx_plot);
Pref_p    = Pref(idx_plot);

fprintf('\nPlotting...\n');

% ============================================================
% Plot settings
% ============================================================

set(groot, 'defaultTextInterpreter', 'latex');
set(groot, 'defaultAxesTickLabelInterpreter', 'latex');
set(groot, 'defaultLegendInterpreter', 'latex');

figW = 7.2;
figH = 5.2;
fontSize = 8;

% ============================================================
% Figure 1: dq variables
% ============================================================

fig = figure('Units', 'inches', ...
             'Position', [1 1 figW figH], ...
             'Color', 'w');

tl = tiledlayout(3, 2, ...
                 'TileSpacing', 'compact', ...
                 'Padding', 'compact');

title(tl, 'CSC-fed PEM Electrolyzer: Electrical Variables', ...
      'FontSize', 12, ...
      'FontWeight', 'bold', ...
      'Interpreter', 'latex');

% ------------------------------------------------------------
% 1) AC-side currents in dq
% ------------------------------------------------------------
nexttile;

plot(tp, id_p, 'LineWidth', 1.5);
hold on;
plot(tp, iq_p, 'LineWidth', 1.5);

grid on;
box on;

xlabel('$t$ [s]');
ylabel('$i_s$ [A]');
title('AC-side currents in $dq$');
legend('$i_{sd}$', '$i_{sq}$', 'Location', 'best');

set(gca, 'FontSize', fontSize, 'LineWidth', 0.8);

% ------------------------------------------------------------
% 2) Active power
% ------------------------------------------------------------
nexttile;

plot(tp, P_p, 'LineWidth', 1.5);
hold on;

grid on;
box on;

xlabel('$t$ [s]');
ylabel('$P$ [W]');
title('Active power');
legend('$P$', 'Location', 'best');

set(gca, 'FontSize', fontSize, 'LineWidth', 0.8);

% ------------------------------------------------------------
% 3) Converter AC capacitor voltage
% ------------------------------------------------------------
nexttile;

plot(tp, vd_p, 'LineWidth', 1.5);
hold on;
plot(tp, vq_p, 'LineWidth', 1.5);

grid on;
box on;

xlabel('$t$ [s]');
ylabel('$u_c$ [V]');
title('Converter AC capacitor voltage in $dq$');
legend('$u_{cd}$', '$u_{cq}$', 'Location', 'best');

set(gca, 'FontSize', fontSize, 'LineWidth', 0.8);

% ------------------------------------------------------------
% 4) Reactive power tracking
% ------------------------------------------------------------
nexttile;

plot(tp, Q_p, 'LineWidth', 1.5);
hold on;
plot(tp, Q_ref_p, '--', 'LineWidth', 1.5);

grid on;
box on;

xlabel('$t$ [s]');
ylabel('$Q$ [var]');
title('Reactive power tracking');
legend('$Q$', '$Q^*$', 'Location', 'best');

set(gca, 'FontSize', fontSize, 'LineWidth', 0.8);

% ------------------------------------------------------------
% 5) DC current tracking
% ------------------------------------------------------------
nexttile;

plot(tp, istk_p, 'LineWidth', 1.5);
hold on;
plot(tp, idc_ref_p, '--', 'LineWidth', 1.5);

grid on;
box on;

xlabel('$t$ [s]');
ylabel('$i_{\mathrm{dc}}$ [A]');
title('DC current tracking');
legend('$i_{\mathrm{dc}}$', '$i_{\mathrm{dc}}^*$', 'Location', 'best');

set(gca, 'FontSize', fontSize, 'LineWidth', 0.8);

% ------------------------------------------------------------
% 6) Electrolyzer DC power
% ------------------------------------------------------------
nexttile;

plot(tp, Pdc_p, 'LineWidth', 1.5);

grid on;
box on;

xlabel('$t$ [s]');
ylabel('$P_{\mathrm{dc}}$ [W]');
title('Electrolyzer DC power');
legend('$P_{\mathrm{dc}}$', 'Location', 'best');

set(gca, 'FontSize', fontSize, 'LineWidth', 0.8);

% ------------------------------------------------------------
% Export figure 1
% ------------------------------------------------------------
exportgraphics(fig, fullfile(figFolder, 'csc_pem_dq0.pdf'), ...
    'ContentType', 'vector');

exportgraphics(fig, fullfile(figFolder, 'csc_pem_dq0.png'), ...
    'Resolution', 300);

% ============================================================
% Figure 2: same layout, but current and voltage in abc
% ============================================================

fig_abc = figure('Units', 'inches', ...
                 'Position', [1 1 figW figH], ...
                 'Color', 'w');

tl_abc = tiledlayout(3, 2, ...
                     'TileSpacing', 'compact', ...
                     'Padding', 'compact');

title(tl_abc, 'CSC-fed PEM Electrolyzer: Electrical Variables in abc', ...
      'FontSize', 12, ...
      'FontWeight', 'bold', ...
      'Interpreter', 'latex');

% ------------------------------------------------------------
% 1) AC-side currents in abc
% ------------------------------------------------------------
nexttile;

plot(tp, ia_p, 'LineWidth', 1.2);
hold on;
plot(tp, ib_p, 'LineWidth', 1.2);
plot(tp, ic_p, 'LineWidth', 1.2);

grid on;
box on;

xlabel('$t$ [s]');
ylabel('$i_s$ [A]');
title('AC-side currents in $abc$');
legend('$i_a$', '$i_b$', '$i_c$', 'Location', 'best');

set(gca, 'FontSize', fontSize, 'LineWidth', 0.8);

% ------------------------------------------------------------
% 2) Active power
% ------------------------------------------------------------
nexttile;

plot(tp, P_p, 'LineWidth', 1.5);

grid on;
box on;

xlabel('$t$ [s]');
ylabel('$P$ [W]');
title('Active power');
legend('$P$', 'Location', 'best');

set(gca, 'FontSize', fontSize, 'LineWidth', 0.8);

% ------------------------------------------------------------
% 3) PCC voltage in abc
% ------------------------------------------------------------
nexttile;

plot(tp, ea_p, 'LineWidth', 1.2);
hold on;
plot(tp, eb_p, 'LineWidth', 1.2);
plot(tp, ec_p, 'LineWidth', 1.2);

grid on;
box on;

xlabel('$t$ [s]');
ylabel('$e_s$ [V]');
title('PCC voltage in $abc$');
legend('$e_a$', '$e_b$', '$e_c$', 'Location', 'best');

set(gca, 'FontSize', fontSize, 'LineWidth', 0.8);

% ------------------------------------------------------------
% 4) Reactive power tracking
% ------------------------------------------------------------
nexttile;

plot(tp, Q_p, 'LineWidth', 1.5);
hold on;
plot(tp, Q_ref_p, '--', 'LineWidth', 1.5);

grid on;
box on;

xlabel('$t$ [s]');
ylabel('$Q$ [var]');
title('Reactive power tracking');
legend('$Q$', '$Q^\star$', 'Location', 'best');

set(gca, 'FontSize', fontSize, 'LineWidth', 0.8);

% ------------------------------------------------------------
% 5) DC current tracking
% ------------------------------------------------------------
nexttile;

plot(tp, istk_p, 'LineWidth', 1.5);
hold on;
plot(tp, idc_ref_p, '--', 'LineWidth', 1.5);

grid on;
box on;

xlabel('$t$ [s]');
ylabel('$i_{\mathrm{dc}}$ [A]');
title('DC current tracking');
legend('$i_{\mathrm{dc}}$', '$i_{\mathrm{dc}}^\star$', 'Location', 'best');

set(gca, 'FontSize', fontSize, 'LineWidth', 0.8);

% ------------------------------------------------------------
% 6) Electrolyzer DC power
% ------------------------------------------------------------
nexttile;

plot(tp, Pdc_p, 'LineWidth', 1.5);

grid on;
box on;

xlabel('$t$ [s]');
ylabel('$P_{\mathrm{dc}}$ [W]');
title('Electrolyzer DC power');
legend('$P_{\mathrm{dc}}$', 'Location', 'best');

set(gca, 'FontSize', fontSize, 'LineWidth', 0.8);

% ------------------------------------------------------------
% Export figure 2
% ------------------------------------------------------------
exportgraphics(fig_abc, fullfile(figFolder, 'csc_pem_abc.pdf'), ...
    'ContentType', 'vector');

exportgraphics(fig_abc, fullfile(figFolder, 'csc_pem_abc.png'), ...
    'Resolution', 300);

% ------------------------------------------------------------
% Print summary
% ------------------------------------------------------------
fprintf('\nPost-processing completed.\n');
fprintf('CSV file: %s\n', csvFile);
fprintf('Figures saved in: %s\n', figFolder);
fprintf('Plotting window: %.6g s to %.6g s\n', t_ini, t_fin);

fprintf('\nFinal states:\n');
fprintf('Final id        = %.6g A\n', id(end));
fprintf('Final iq        = %.6g A\n', iq(end));
fprintf('Final vd        = %.6g V\n', vd(end));
fprintf('Final vq        = %.6g V\n', vq(end));
fprintf('Final istk      = %.6g A\n', istk(end));
fprintf('Final theta_hat = %.6g rad\n', theta_hat(end));
fprintf('Final xi_pll    = %.6g\n', xi_pll(end));

fprintf('\nFinal derived variables:\n');
fprintf('Final P         = %.6g W\n', P(end));
fprintf('Final Q         = %.6g var\n', Q(end));
fprintf('Final Pdc       = %.6g W\n', Pdc(end));